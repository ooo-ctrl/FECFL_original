class SimpleMLP(nn.Module):
    def __init__(self):
        super(SimpleMLP, self).__init__()
        # Input layer to hidden layer
        self.fc1 = nn.Linear(28 * 28, 512)  # 28*28 pixels in, 512 units in hidden layer
        # Hidden layer to output layer
        self.fc2 = nn.Linear(512, 10)  # 512 units in hidden layer, 10 units out for the number of classes

    def forward(self, x):
        x = x.view(-1, 28 * 28)  # Flatten the images into a 784 length vector
        x = F.relu(self.fc1(x))  # ReLU activation function for hidden layer
        x = self.fc2(x)  # Output layer, no activation function here
        return x

    def extract_features(self, x):
        x = x.view(-1, 28 * 28)
        x = F.relu(self.fc1(x))
        return x
